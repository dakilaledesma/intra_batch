def _resnet(arch, block, layers, pretrained, progress, last_stride=0, neck=0,
            red=1, **kwargs):
    model = ConvNeXt(depths=[3, 3, 27, 3], dims=[128, 256, 512, 1024], last_stride=last_stride, neck=neck, 
            red=red, **kwargs)
    if pretrained:
        url = model_urls['convnext_base_22k'] if in_22k else model_urls['convnext_base_1k']
    if pretrained:
        if not neck:
            url = model_urls['convnext_base_22k'] if in_22k else model_urls['convnext_base_1k']
            state_dict = torch.hub.load_state_dict_from_url(url=url, map_location="cpu", progress=progress)
            for i in state_dict:
                if 'fc' in i:
                    continue
                model.state_dict()[i].copy_(state_dict[i])
        else:
            url = model_urls['convnext_base_22k'] if in_22k else model_urls['convnext_base_1k']
            state_dict = load_state_dict_from_url(model_urls[arch],
                                                  progress=progress)
            for i in state_dict:
                if 'fc' in i:
                    continue
                model.state_dict()[i].copy_(state_dict[i])
    return model


